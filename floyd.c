int Cost[SIZE][SIZE]; /* 任意の２頂点間の最小経路コスト */
int PV[SIZE][SIZE];	  /* Pre-Vertexの意味．最短経路における1つ前のノード */
					  /* PV[i][j]は，ノードiからノードjまでの最短経路    */
					  /* においてjの1つ前のノードを表す                  */

/*
 *	GraphData[]の表示
 */
void PrintGraphData(void)
{
	int i;

	for (i = 0; i < SIZE; i++)
	{
		printf("%2d : %s\n", i, GraphData[i]);
	}
}

/*
 *	Cost[][]とPV[][]の初期化
 */
void Initialize(void)
{
	int i, j;
	for (i = 0; i < SIZE; i++)
	{
		for (j = 0; j < SIZE; j++)
		{
			Cost[i][j] = Adjacent[i][j];
			PV[i][j] = i;
		}
	}
}

/*
 *	Cost[][]とPV[][]の表示
 */
void PrintCostPV(int n)
{
	int i, j;

	printf("====== n=%d ======\n", n);
	printf("Cost[][]");
	for (i = 0; i < 63; i++)
		putchar(' ');
	printf("PV[][]\n");

	for (i = 0; i < SIZE; i++)
	{
		for (j = 0; j < SIZE; j++)
		{
			if (Cost[i][j] == IMAX)
			{
				printf(" ∞, ");
			}
			else
			{
				printf("%3d, ", Cost[i][j]);
			}
		}
		printf("     ");
		for (j = 0; j < SIZE; j++)
		{
			printf("%2d, ", PV[i][j]);
		}
		printf("\n");
	}
	printf("\n");
}

/*
 *	すべての頂点間の最短経路をフロイド法で探索する
 */
void Floyd(void)
{
	int n = 0, i, j;
	PrintCostPV(n);
	for (n = 1; n < SIZE + 1; n++)
	{
		for (i = 0; i < SIZE; i++)
		{
			for (j = 0; j < SIZE; j++)
			{
				if (Cost[i][n - 1] + Cost[n - 1][j] < Cost[i][j])
				{
					Cost[i][j] = Cost[i][n - 1] + Cost[n - 1][j];
					Cost[j][i] = Cost[j][n - 1] + Cost[n - 1][i];
					PV[i][j] = PV[n - 1][j];
					PV[j][i] = PV[n - 1][i];
				}
			}
		}
		PrintCostPV(n);
	}
}

/*
 *	最短経路の表示
 */
void PrintPath(int start, int goal)
{
	if (start != goal)
	{
		PrintPath(start, PV[start][goal]);
		printf(" -> %s", GraphData[goal]);
	}
	else if (start == goal)
	{
		printf("%s", GraphData[start]);
		return;
	}
}

int main(void)
{
	int start, goal;

	Initialize();	  /* Cost[][]とPV[][]の初期化 */
	Floyd();		  /* フロイド法による探索 */
	PrintGraphData(); /* 都県名と番号の表示 */

	/*
	 *	出発地と目的地の入力
	 */
	printf("出発地を0～12の番号で入力してください：");
	scanf("%d", &start);
	printf("目的地を0～12の番号で入力してください：");
	scanf("%d", &goal);

	/*
	 *	出発地から目的地までの最短経路とコストの表示
	 */
	PrintPath(start, goal);
	printf("\n");
	printf("コストは %d です．\n", Cost[start][goal]);

	return 0;
}

